  <h1>CSCI 466 Group Project</h1>
  <p>
    This is the group project for CSCI 466. The goal is to create a simple e-commerce application using PHP and MySQL.
    The application will allow users to browse products, add them to a shopping cart, and place orders. Employees will be able to manage inventory and fulfill orders.
  </p>

  <h2>Application Overview</h2>
  <ul>
    <li><strong>Inventory:</strong> View and browse products, see stock levels</li>
    <li><strong>Shopping Cart:</strong> Add/remove items, update quantities</li>
    <li><strong>Checkout:</strong> Enter shipping &amp; billing info, place orders</li>
    <li><strong>Orders:</strong>
      <ul>
        <li>User‑side: track your orders and their status</li>
        <li>Employee‑side: list outstanding orders &amp; fulfill them</li>
      </ul>
    </li>
  </ul>

 
